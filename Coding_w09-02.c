#include <stdio.h>

int main() {
int sum, p, x, y;
sum = 0;
for (x = 1, y = 1; x * y <= 15; x++, y += 2)
{
    printf("round %d : " ,x);
p = x * y;
sum = sum + p;

printf("sum = %d, x = %d, y = %d, p = %d\n",sum, x, y, p);

printf("%d * %d = %d\n", x, y, p);
}
printf("summation of x * y = %d\n", sum);
return 0;
}
