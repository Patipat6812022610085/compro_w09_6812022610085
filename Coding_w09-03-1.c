#include <stdio.h>

int main() {
int i;
for (i = 1; i <= 10; i++) {
if (i == 6) {
continue;
}
printf("%d ", i);
}
return 0;
}
/*
1.เริ่มต้น i = 1

2.ตรวจเงื่อนไข i <= 10 (จริง → เข้าลูป)

3.ถ้า i == 6 → ข้าม (continue) ไม่พิมพ์ค่า

4.ถ้าไม่ใช่ 6 → พิมพ์ค่า i

5.เพิ่มค่า i++ ทุกครั้งที่วนลูป

6.ทำต่อจน i > 10 จบลูป
*/