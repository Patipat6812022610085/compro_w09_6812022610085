#include <stdio.h>

int main() {
int i = 1;
while (i <= 10) {
if (i == 6) {
i++; // ต้องเพิ่มค่า i ก่อน ไม่งั้นจะวนค้างที่ 6
continue;
}
printf("%d ", i);
i++;
}
return 0;
}
/*
1.เริ่มต้น i = 1

2.ตรวจเงื่อนไข i <= 10 (จริง → เข้าลูป)

3.ถ้า i == 6 → เพิ่มค่า i++ แล้ว continue (กระโดดไปเช็กเงื่อนไขใหม่ทันที)

4.ถ้าไม่ใช่ 6 → พิมพ์ค่า i แล้วค่อย i++

5.ทำต่อจน i > 10 จบลูป
*/